#include <stdio.h>

int main()
{
	int x = 10;
	float y = 5.0e+4;

	/* escrenvendo na tela o valor da variável x */
	printf("%d\n", x);

	/* escrevendo na tela o valor da variável y */
	printf("%f\n", y);

	/* escrevendo na tela os dois valores separados por espaço */
	printf("%d %f\n", x, y);

	return 0;
}